public class Coordinate {
  private double x;
  private double y;

  public Coordinate(double x_, double y_) {
    x = x_;
    y = y_;
  }

  public double getX() {
    return x;
  }

  public double getY() {
    return y;
  }

  public void setX(double x_) {
    x = x_;
  }

  public void setY(double y_) {
    y = y_;
  }

  public void setCoord(double x_, double y_) {
    x = x_;
    y = y_;
  }
}
